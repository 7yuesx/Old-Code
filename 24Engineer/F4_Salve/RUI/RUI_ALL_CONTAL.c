uint8_t RUI_F_TOTAL_CONTRAL(void)
{
    uint8_t RUI_V_DBUS_STATUS = RUI_ROOT.STATUS.RM_DBUS;
    switch (RUI_ROOT.MASTER_LOCATION)
    {
        case RUI_DF_MASTER_LOCATION_BOTTOM:
            {
                RUI_F_BOTTOM_CONTRAL(RUI_V_DBUS_STATUS);
                return RUI_DF_READY;
            }break;
    }


    return RUI_DF_ERROR;

}


