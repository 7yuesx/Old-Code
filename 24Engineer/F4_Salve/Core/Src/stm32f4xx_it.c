/**
  * @brief This function handles UART4 global interrupt.
  */
void UART4_IRQHandler(void)
{
  /* USER CODE BEGIN UART4_IRQn 0 */
	if (__HAL_UART_GET_FLAG(&huart4 , UART_FLAG_IDLE) == 1)
	{
		__HAL_UART_CLEAR_IDLEFLAG(&huart4);                     //清楚空闲中断标志（否则会一直不断进入中断）
		HAL_UART_DMAStop(&huart4);
		uint8_t data_length  = 255 - __HAL_DMA_GET_COUNTER(&hdma_uart4_rx);   //计算接收到的数据长度
		memcpy(ALL_RX_Data.Data , receive_buff , data_length);
		Read_Data_system(&ALL_RX_Data , &user_data);
		memset(receive_buff,0,data_length);                                    //清零接收缓冲区
		data_length = 0;
		HAL_UART_Receive_DMA(&huart4 , receive_buff , 255);
	}
  /* USER CODE END UART4_IRQn 0 */
  HAL_UART_IRQHandler(&huart4);
  /* USER CODE BEGIN UART4_IRQn 1 */

  /* USER CODE END UART4_IRQn 1 */
}

